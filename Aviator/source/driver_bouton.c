void InitBoutons()
{
  P1DIR &= ~(BIT6+BIT7);       //direction du port
  
  P1IE |= (BIT6+BIT7);         //activation de l'int
  P1IES |= (BIT6+BIT7);        //Sur front descendant
  P1IFG &= ~(BIT6+BIT7);       //reset du flag
}

